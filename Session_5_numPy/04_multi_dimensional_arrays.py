import numpy as np

# 1D array (vector)
vector = np.array([1, 2, 3])
print("1D array:")
print(vector)
print("Number of dimensions:", vector.ndim)

# 2D array (matrix)
matrix = np.array([[1, 2, 3], [4, 5, 6]])
print("\n2D array:")
print(matrix)
print("Number of dimensions:", matrix.ndim)

# 3D array (tensor)
tensor = np.array([[[1, 2], [3, 4]], [[5, 6], [7, 8]]])
print("\n3D array:")
print(tensor)
print("Number of dimensions:", tensor.ndim)

