# Exercise 2: Tuple Concatenation
tuple1 = (1, 2, 3, 4, 5)
tuple2 = (4, 5, 6, 7, 8)
concatenated_tuple = tuple1 + tuple2
print("Concatenated tuple:", concatenated_tuple)


# This Python code concatenates two tuples into a single tuple. Here's a breakdown:
#
# `tuple1 = (1, 2, 3, 4, 5)`:
#       Defines the first tuple `tuple1`.
#
# `tuple2 = (4, 5, 6, 7, 8)`:
#       Defines the second tuple `tuple2`.
#
# `concatenated_tuple = tuple1 + tuple2`:
#       Concatenates `tuple1` and `tuple2` using the `+` operator,
#       resulting in a new tuple `concatenated_tuple`.
#       - When you concatenate two tuples in Python,
#         it creates a new tuple containing all the elements from both tuples
#         in the order they appear.
